
import java.lang.*;

public class Test01 {

	public static void main(String[] args) {
		
		// 컴퓨터에서 자주 사용되는 데이터 타입
		//정수
		System.out.println(10);
		//실수
		System.out.println(12.34);
		//문자
		// - 문자형데이터를 사용할때는 ''안에 적어야 문자로 인식된다...
		System.out.println('S');
		//문자열
		// - 모든 프로그래밍언어에서는 문장을 문자열이라 부른다...
		// - 문자열데이터를 사용할때는 ""안에 적어야 문자열로 인식된다...
		System.out.println("System.out.println");
		
		// . (멤버 참조 연산자)
		// - 클래스의 멤버를 참조(호출)할때 사용하는 연산자.
		
		// package - 비슷한 성격의 파일들을 모아놓은 폴더
		// - package 명은 반드시 소문자로 작성한다.
		
	}
	
}
